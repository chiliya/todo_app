<template>
    <!-- 梯度颜色组件 -->
  <div class="gradient_color"
       :class="{ gradient_color__active: active }" 
       :style="{ backgroundImage: gradientColor }">
       </div>
</template>

<script>
export default {
    data () {
    return {

    }
  },
  props: {
      colors: {
          type: Array
      },
      active: {
          type: Boolean
      }
  },
  computed: {
      gradientColor() {
          const colorBottom = `color-stop(30%, ${this.colors[0]})`
          const colorTop = `to(${this.colors[1]})`
          return `-webkit-gradient(linear, left bottom, right top, ${colorBottom}, ${colorTop})`
      }
  }
}

</script>

<style lang="less">
.gradient_color {
    position: absolute;
    width: 100%;
    height: 100%;
    opacity: 0;
    transition: opacity 0.5s ease;
}

.gradient_color__active {
    opacity: 1;
}
</style>
